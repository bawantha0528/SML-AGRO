const normalize = (value) => String(value || '').trim().toLowerCase();

const findRawCategoryByName = (name) => {
  const key = normalize(name);
  if (key.includes('peat') || key.includes('pith') || key.includes('coco pit')) return 'COCOPEAT';
  if (key.includes('fiber')) return 'FIBER';
  if (key.includes('grow bag') || key.includes('growbag')) return 'GROW_BAGS';
  if (key.includes('mat')) return 'MATS';
  if (key.includes('rope') || key.includes('twine')) return 'ROPE';
  if (key.includes('geotextile') || key.includes('geo textile')) return 'GEOTEXTILES';
  return 'DEFAULT';
};

export const getProductGallery = (product = {}) => {
  if (Array.isArray(product.gallery) && product.gallery.length > 0) {
    return product.gallery;
  }

  const explicitImage = product.imageUrl || product.image;
  if (explicitImage) {
    return [explicitImage, explicitImage, explicitImage];
  }

  const isCustom = product.productKind === 'custom' || product.category === 'CUSTOM';
  if (isCustom) {
    return CUSTOM_PRODUCT_IMAGES[product.name] || CUSTOM_PRODUCT_IMAGES.DEFAULT;
  }

  const category = product.category || findRawCategoryByName(product.name);
  return RAW_PRODUCT_IMAGES[category] || RAW_PRODUCT_IMAGES.DEFAULT;
};

export const getProductImage = (product = {}) => getProductGallery(product)[0];

export const getCustomCollectionImage = (name) => {
  const gallery = CUSTOM_PRODUCT_IMAGES[name] || CUSTOM_PRODUCT_IMAGES.DEFAULT;
  return gallery[0];
};
